even = [2, 4, 6, 8]
odd = [1, 3, 5, 7, 9]

# returns min and max
print(min(even))
print(max(even))
print(min(odd))
print(max(odd))

# len returns number of items in sequence. for a string its the number of characters
print(len(even))
print(len(odd))

# returns an int of the amount of 's' in string
print("mississippi".count("s"))
